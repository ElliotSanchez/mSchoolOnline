CREATE TABLE `ttm_student` (
  `id` int(11) unsigned NOT NULL AUTO_INCREMENT,
  `import_filename` varchar(256) DEFAULT NULL,
  `imported_at` datetime DEFAULT NULL,
  `student_id` int(11) DEFAULT NULL,
  `student_name` varchar(256) DEFAULT '',
  `grade_name` varchar(256) DEFAULT '',
  `classroom_name` varchar(256) DEFAULT '',
  `pathway_name` varchar(256) DEFAULT '',
  `grade_level_deviation` varchar(256) DEFAULT '',
  `lesson_name` varchar(256) DEFAULT '',
  `type` varchar(256) DEFAULT '',
  `tested_out` varchar(256) DEFAULT '',
  `passed` varchar(256) DEFAULT '',
  `pre_quiz_score` varchar(256) DEFAULT '',
  `post_quiz_score` varchar(256) DEFAULT '',
  `time_on_system` varchar(256) DEFAULT '',
  `date_started` varchar(256) DEFAULT '',
  `date_completed` varchar(256) DEFAULT '',
  `created_at` datetime NOT NULL,
  `updated_at` datetime DEFAULT NULL,
  PRIMARY KEY (`id`),
  KEY `student_id` (`student_id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8;